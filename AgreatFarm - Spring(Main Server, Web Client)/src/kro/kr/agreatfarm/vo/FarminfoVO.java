package kro.kr.agreatfarm.vo;

public class FarminfoVO {
	private int 	m_num, f_num;
	private String 	f_url, f_cagename, f_type, f_count, f_location;
	
	public int getM_num() {
		return m_num;
	}
	public void setM_num(int m_num) {
		this.m_num = m_num;
	}
	public int getF_num() {
		return f_num;
	}
	public void setF_num(int f_num) {
		this.f_num = f_num;
	}
	public String getF_url() {
		return f_url;
	}
	public void setF_url(String f_url) {
		this.f_url = f_url;
	}
	public String getF_cagename() {
		return f_cagename;
	}
	public void setF_cagename(String f_cagename) {
		this.f_cagename = f_cagename;
	}
	public String getF_type() {
		return f_type;
	}
	public void setF_type(String f_type) {
		this.f_type = f_type;
	}
	public String getF_count() {
		return f_count;
	}
	public void setF_count(String f_count) {
		this.f_count = f_count;
	}
	public String getF_location() {
		return f_location;
	}
	public void setF_location(String f_location) {
		this.f_location = f_location;
	}
}
